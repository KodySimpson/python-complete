# Comments in Python
# Comments explain code and are ignored by Python.
# Use them to make your code readable!

# Single-line comment: Starts with # and goes to the end of the line.
print("This runs!")  # This is an inline comment â€“ code before it executes.

# Multi-line comments: No built-in syntax, but use multiple # lines...
